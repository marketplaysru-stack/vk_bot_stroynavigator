import random

# Стили для строительной тематики
STYLES = [
    "архитектурная фотография",
    "индустриальный пейзаж",
    "инфографика с элементами строительства",
    "макро-съёмка материалов",
    "схематичный чертёж",
    "урбанистический скетч",
    "деталь инструмента",
    "строительная площадка с высоты птичьего полёта",
    "интерьер после ремонта",
    "технологичная иллюстрация",
]

def get_context_from_topic(topic):
    keywords = []
    if "фундамент" in topic:
        keywords.append("фундамент, бетон, арматура")
    if "стен" in topic:
        keywords.append("стены, кирпич, кладка")
    if "кровля" in topic or "крыш" in topic:
        keywords.append("кровля, черепица, стропила")
    if "электрик" in topic:
        keywords.append("электропроводка, щит, автоматы")
    if "сантехник" in topic:
        keywords.append("трубы, сантехника, канализация")
    if "ремонт" in topic:
        keywords.append("отделка, интерьер, дизайн")
    if "СНИП" in topic or "ГОСТ" in topic:
        keywords.append("нормы, правила, стандарты")
    if "ошибк" in topic:
        keywords.append("строительные ошибки, дефекты")
    if not keywords:
        keywords.append("строительство, объекты, материалы")
    return ", ".join(keywords)

def build_image_prompt(topic):
    style = random.choice(STYLES)
    context = get_context_from_topic(topic)

    base = (
        f"Создай изображение для поста на тему: '{topic}'. "
        f"Стиль: {style}. "
        f"Учти контекст: {context}. "
        "Изображение должно быть уникальным, не использовать распространённые стоковые клише. "
        "Предпочтение отдаётся беслюдным изображениям: объекты, материалы, инструменты, архитектура, чертежи, схемы. "
        "Если присутствуют люди – только рабочие в касках, разнообразные по полу и возрасту, но лучше избегать. "
        "Цветовая палитра: индустриальная, натуральная (бетон, дерево, металл). "
        "Формат: квадратный (1:1). "
        "Высокое качество, 8K, фотореализм или векторная графика – в зависимости от стиля. "
        "Без текста и надписей."
    )

    details = [
        "с акцентом на текстуру материалов",
        "с естественным освещением",
        "с геометрическими формами",
        "с крупным планом",
        "с динамичной композицией",
        "с использованием негативного пространства",
        "с контрастными тенями",
    ]
    base += " " + random.choice(details)

    if random.random() < 0.3:
        base += " Действие происходит на стройплощадке или в интерьере."

    return base

