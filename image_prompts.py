import random

# Стили с жёстким требованием фотореализма и детализации
STYLES = [
    "фотореалистичная архитектурная фотография, сверхдетализированная, 8K",
    "индустриальный пейзаж с естественными текстурами, профессиональная съёмка",
    "макро-съёмка строительных материалов с видимыми порами и микро-трещинами",
    "кинематографичный кадр стройплощадки, золотой час, глубокая резкость",
    "деталь инструмента или конструкции, крупный план, фотореализм",
    "интерьер после ремонта, студийное освещение, чёткие тени",
    "крупный план бетона, арматуры, древесины с естественными дефектами",
    "панорама стройки с дрона, высокое разрешение, естественные цвета",
]

def get_context_from_topic(topic):
    keywords = []
    if "фундамент" in topic:
        keywords.append("фундамент, бетон, арматура, текстура бетона")
    if "стен" in topic:
        keywords.append("стены, кирпичная кладка, штукатурка, фактура")
    if "кровля" in topic or "крыш" in topic:
        keywords.append("кровля, черепица, металл, стропила")
    if "электрик" in topic:
        keywords.append("электропроводка, кабели, щит, автоматы")
    if "сантехник" in topic:
        keywords.append("трубы, фитинги, сантехника, вода")
    if "ремонт" in topic:
        keywords.append("отделка, плитка, обои, напольные покрытия")
    if "СНИП" in topic or "ГОСТ" in topic:
        keywords.append("строительные нормы, документация, чертежи")
    if "ошибк" in topic:
        keywords.append("дефекты, трещины, разрушения, проблемы")
    if not keywords:
        keywords.append("строительные объекты, материалы, конструкции")
    return ", ".join(keywords)

def build_image_prompt(topic):
    style = random.choice(STYLES)
    context = get_context_from_topic(topic)

    base = (
        f"Создай фотореалистичное изображение для поста на тему: '{topic}'. "
        f"Стиль: {style}. "
        f"Контекст: {context}. "
        "Изображение должно быть максимально детализированным, с чёткими текстурами, естественным освещением, "
        "глубиной резкости (shallow depth of field), профессиональной композицией. "
        "Цвета естественные, насыщенные, без мультяшности, без иллюстративности. "
        "Используй реалистичные материалы: бетон, дерево, металл, стекло, кирпич. "
        "Добавь микро-детали: царапины, пыль, капли воды, тени, отражения, фактуру поверхности. "
        "Формат: квадратный (1:1). "
        "Разрешение: 8K, сверхвысокое качество. "
        "Без текста и надписей. "
        "Фотореализм, не мультяшный стиль, не иллюстрация, а фотография, сделанная на профессиональную камеру."
    )

    details = [
        "с акцентом на текстуру материалов",
        "с драматичным контровым освещением",
        "с крупным планом для демонстрации деталей",
        "с естественным дневным светом, мягкими тенями",
        "с тёплым золотистым оттенком (золотой час)",
        "с холодным индустриальным светом",
        "с глубиной резкости, фон размыт в боке",
        "с высокой контрастностью для объёма",
    ]
    base += " " + random.choice(details)

    if random.random() < 0.3:
        base += " Действие происходит на реальной стройплощадке или в современном интерьере."

    # Явный запрет на мультяшность
    base += " Запрещены мультяшные стили, векторная графика, плоские иллюстрации. Только фотореализм."

    return base

